/// Return pair index (0-5) or None if non-canonical.
/// A=0, C=1, G=2, U=3  →  AU=0, UA=1, CG=2, GC=3, GU=4, UG=5
#[inline]
pub fn pair_index(bi: u8, bj: u8) -> Option<usize> {
    match (bi, bj) {
        (0, 3) => Some(0), // AU
        (3, 0) => Some(1), // UA
        (1, 2) => Some(2), // CG
        (2, 1) => Some(3), // GC
        (2, 3) => Some(4), // GU
        (3, 2) => Some(5), // UG
        _ => None,
    }
}

#[inline]
pub fn is_au_gu(pi: usize) -> bool {
    matches!(pi, 0 | 1 | 4 | 5)
}

/// Triloop special bonus (5-mer window seq[i..=j], size==3).
/// Keys encoded as [b0,b1,b2,b3,b4] with A=0,C=1,G=2,U=3.
#[inline]
pub fn triloop_bonus(w: &[u8]) -> i32 {
    match w {
        [1, 0, 0, 1, 2] => 680, // CAACG
        [2, 3, 3, 0, 1] => 690, // GUUAC
        _ => 0,
    }
}

/// Tetraloop special bonus (6-mer window seq[i..=j], size==4).
