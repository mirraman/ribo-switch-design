#[inline]
pub fn pair_index(bi: u8, bj: u8) -> Option<usize> {
    match (bi, bj) {
        (0, 3) => Some(0),
        (3, 0) => Some(1),
        (1, 2) => Some(2),
        (2, 1) => Some(3),
        (2, 3) => Some(4),
        (3, 2) => Some(5),
        _ => None,
    }
}
